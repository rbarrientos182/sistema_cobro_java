public void loguearseSistema() 
{
        String user = vUser.getText();
        String pass = vPaswd.getText();
        String clave = "zolbee";

        LeeFichero leerFichero = new LeeFichero ();
        Funciones funcion = new Funciones();
        Funciones funcion2 = new Funciones();
        leerFichero.leerFichero();
        funcion.EncriptCadena(clave,user);
        funcion2.EncriptCadena(clave, pass);
            

        if(funcion.encriptado.equals(leerFichero.vusuario) && funcion2.encriptado.equals(leerFichero.vpaswd))
        {

            switch(opcion)
            {
                case(1):
                     ReportesFecha fecha = new ReportesFecha ();
                     fecha.setLocationRelativeTo(null);
                     fecha.getContentPane().setBackground(Color.decode("#7329ab"));
                     fecha.setVisible(true);
                     break;
                case (2):
                     ReportesEmpleado empleado = new ReportesEmpleado ();
                     empleado.setLocationRelativeTo(null);
                     empleado.getContentPane().setBackground(Color.decode("#7329ab"));
                     empleado.setVisible(true);
                     break;
                case (3):
                    ReporteSocios socio = new ReporteSocios();
                    socio.setLocationRelativeTo(null);
                    socio.getContentPane().setBackground(Color.decode("#7329ab"));
                    socio.setVisible(true);
                    break;           
            } // fin de switch
            this.dispose();
        }

        else
        {
            Funciones.imprimirMsj("Lo sentimos el usuario o contrase√±a son incorrectos");
        }
      }

